#coding:utf-8
import jieba

with open('10.txt','r',encoding='utf-8') as f:
    # print(len(f.readlines()))
    # print(f.readlines())
    temp=f.readlines()
    with open('final_10.txt','w',encoding='utf-8') as f_final:
        for i in temp:
            data=jieba.cut(i)
            str_temp=''
            for j in data:
                if j!='\n':
                    str_temp+=j+' '
                else:
                    str_temp += j

            # str_temp+='\n'
            # print(str_temp)
            f_final.write(str_temp)

